# ── Helpers ───────────────────────────────────────────────────────────────────
def heading(level: int, text: str):
    colors = {1: BLUE_DARK, 2: BLUE_MID, 3: RGBColor(0x2F, 0x54, 0x96)}
    p = doc.add_heading(text, level=level)
    for run in p.runs:
        run.font.color.rgb = colors[level]
    return p


def para(
    text: str,
    size: int = 11,
    bold: bool = False,
    italic: bool = False,
    color: RGBColor | None = None,
):
    p = doc.add_paragraph()
    run = p.add_run(text)
    run.font.size = Pt(size)
    run.bold = bold
    run.italic = italic
    if color:
        run.font.color.rgb = color
    return p


def bullet(text: str, size: int = 11):
    p = doc.add_paragraph(style="List Bullet")
    run = p.add_run(text)
    run.font.size = Pt(size)
    return p


def interp_box(lines: list[str], bg: str = GREEN_LIGHT, label: str = "Бизнес-интерпретация"):
    """Shaded box with business interpretation text."""
    # label row
    tbl = doc.add_table(rows=1, cols=1)
    tbl.style = "Table Grid"
    tbl.alignment = WD_TABLE_ALIGNMENT.CENTER
    hdr_cell = tbl.rows[0].cells[0]
    hdr_cell.text = ""
    p_lbl = hdr_cell.paragraphs[0]
    r = p_lbl.add_run(f"  {label}")
    r.bold = True
    r.font.size = Pt(10)
    r.font.color.rgb = RGBColor(0x1F, 0x38, 0x64)
    _shade_cell(hdr_cell, "BDD7EE")

    for line in lines:
        row = tbl.add_row()
        cell = row.cells[0]
        p = cell.paragraphs[0]
        p.paragraph_format.left_indent = Pt(8)
        run = p.add_run(f"  {line}")
        run.font.size = Pt(10)
        _shade_cell(cell, bg)

    doc.add_paragraph()


def _shade_cell(cell, fill: str):
    tc = cell._tc
    tcPr = tc.get_or_add_tcPr()
    shd = OxmlElement("w:shd")
    shd.set(qn("w:val"), "clear")
    shd.set(qn("w:color"), "auto")
    shd.set(qn("w:fill"), fill)
    tcPr.append(shd)


def add_table(headers: list, rows: list, col_widths: list | None = None) -> None:
    tbl = doc.add_table(rows=1, cols=len(headers))
    tbl.style = "Table Grid"
    tbl.alignment = WD_TABLE_ALIGNMENT.CENTER
    hdr_row = tbl.rows[0]
    for i, text in enumerate(headers):
        cell = hdr_row.cells[i]
        cell.text = str(text)
        run = cell.paragraphs[0].runs[0]
        run.bold = True
        run.font.size = Pt(10)
        _shade_cell(cell, BLUE_LIGHT)
    for row_data in rows:
        row = tbl.add_row()
        for i, text in enumerate(row_data):
            cell = row.cells[i]
            cell.text = str(text)
            cell.paragraphs[0].runs[0].font.size = Pt(10)
    doc.add_paragraph()


